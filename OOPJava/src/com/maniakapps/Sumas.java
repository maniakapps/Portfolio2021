package com.maniakapps;

public class Sumas {
    public double sumaCubica(double num1, double num2, double num3){
        return Math.pow(num1, 3) + Math.pow(num2, 3) + Math.pow(num3, 3);
    }
    public int sumaNumeros(int num1, int num2, int num3){
        return num1 + num2 + num3;
    }
}
